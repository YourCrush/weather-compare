export const ViewToggle: React.FC = () => {
  const { state, setActiveView } = useAppContext();
  const { activeView } = state.ui;

  return (
    <div className="flex bg-gray-100 dark:bg-gray-700 rounded-lg p-1" role="tablist">
      {viewOptions.map((option) => (
        <button
          key={option.key}
          onClick={() => setActiveView(option.key)}
          className={`
            flex items-center space-x-2 px-3 py-2 rounded-md text-sm font-medium transition-all duration-200
            ${
              activeView === option.key
                ? 'bg-white dark:bg-gray-600 text-primary-600 dark:text-primary-400 shadow-sm'
                : 'text-gray-500 dark:text-gray-400 hover:text-gray-700 dark:hover:text-gray-300 hover:bg-gray-50 dark:hover:bg-gray-600'
            }
          `}
          role="tab"
          aria-selected={activeView === option.key}
          aria-label={`Switch to ${option.label} view`}
        >
          {option.icon}
          <span className="hidden sm:inline">{option.label}</span>
        </button>
      ))}
    </div>
  );
};